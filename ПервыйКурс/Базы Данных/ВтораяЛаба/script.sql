"Н_ЛЮДИ"."ОТЧЕСТВО" < 'Сергеевич' and "Н_СЕССИЯ"."УЧГОД" > '2001/2002'
and "Н_СЕССИЯ"."УЧГОД" = '2011/2012'

2.select "Н_ЛЮДИ".ИД , "Н_ОБУЧЕНИЯ".НЗК , "Н_УЧЕНИКИ".ИД from "Н_ЛЮДИ"
left join "Н_УЧЕНИКИ" on "Н_ЛЮДИ"."ИД"="Н_УЧЕНИКИ"."ЧЛВК_ИД" left join
"Н_ОБУЧЕНИЯ" on "Н_УЧЕНИКИ"."ЧЛВК_ИД" = "Н_ОБУЧЕНИЯ"."ЧЛВК_ИД" where
"Н_ЛЮДИ"."ИМЯ" = 'Ярослав' and "Н_ОБУЧЕНИЯ"."НЗК" > '999080'

