5. SELECT ОЦЕНОЧКИ.ИД, ОЦЕНОЧКИ.ГРУППА, ОЦЕНОЧКИ.ИМЯ, ОЦЕНОЧКИ.ФАМИЛИЯ,
ОЦЕНОЧКИ.ОТЧЕСТВО, AVG(ОЦЕНОЧКИ.ОЦЕН) AS "СР_ОЦЕНКА" FROM (
SELECT Н_ЛЮДИ.ИД, Н_УЧЕНИКИ.ГРУППА, Н_ЛЮДИ.ИМЯ, Н_ЛЮДИ.ФАМИЛИЯ,
Н_ЛЮДИ.ОТЧЕСТВО, CAST(Н_ВЕДОМОСТИ.ОЦЕНКА AS INTEGER) AS ОЦЕН FROM Н_ВЕДОМОСТИ
JOIN Н_УЧЕНИКИ ON Н_УЧЕНИКИ.ЧЛВК_ИД = Н_ВЕДОМОСТИ.ЧЛВК_ИД AND
Н_УЧЕНИКИ.ГРУППА = '4100'
JOIN Н_ЛЮДИ ON Н_УЧЕНИКИ.ЧЛВК_ИД = Н_ЛЮДИ.ИД
WHERE Н_ВЕДОМОСТИ.ОЦЕНКА IN ('2', '3', '4', '5')
) AS ОЦЕНОЧКИ
GROUP BY
ОЦЕНОЧКИ.ИД,
ОЦЕНОЧКИ.ГРУППА,
ОЦЕНОЧКИ.ИМЯ,
ОЦЕНОЧКИ.ФАМИЛИЯ,
ОЦЕНОЧКИ.ОТЧЕСТВО

HAVING AVG(ОЦЕНОЧКИ.ОЦЕН) <= (SELECT MIN(CAST(Н_ВЕДОМОСТИ.ОЦЕНКА AS
INTEGER)) FROM Н_ВЕДОМОСТИ
JOIN
Н_УЧЕНИКИ ON Н_УЧЕНИКИ.ЧЛВК_ИД = Н_ВЕДОМОСТИ.ЧЛВК_ИД AND Н_УЧЕНИКИ.ГРУППА =
'3100'
WHERE Н_ВЕДОМОСТИ.ОЦЕНКА IN ('2', '3', '4', '5'));

